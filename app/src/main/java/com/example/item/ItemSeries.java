package com.example.item;

public class ItemSeries {

    private String id;
    private String seriesName;
    private String seriesDesc;
    private String seriesPoster;
    private String seriesCover;
    private String totalViews;
    private String rateAvg;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSeriesName() {
        return seriesName;
    }

    public void setSeriesName(String seriesName) {
        this.seriesName = seriesName;
    }

    public String getSeriesDesc() {
        return seriesDesc;
    }

    public void setSeriesDesc(String seriesDesc) {
        this.seriesDesc = seriesDesc;
    }

    public String getSeriesPoster() {
        return seriesPoster;
    }

    public void setSeriesPoster(String seriesPoster) {
        this.seriesPoster = seriesPoster;
    }

    public String getSeriesCover() {
        return seriesCover;
    }

    public void setSeriesCover(String seriesCover) {
        this.seriesCover = seriesCover;
    }

    public String getTotalViews() {
        return totalViews;
    }

    public void setTotalViews(String totalViews) {
        this.totalViews = totalViews;
    }

    public String getRateAvg() {
        return rateAvg;
    }

    public void setRateAvg(String rateAvg) {
        this.rateAvg = rateAvg;
    }
}
